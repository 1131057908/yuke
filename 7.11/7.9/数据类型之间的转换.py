# 整数，小数，字符串之间的转换：
# str（）：用于讲一个变量转换成字符串类型的数据
a=10
print(type(a))
a1=str(a)
print('a转换之后的类型',type(a1))
b=9.88
print(type(b))
b1=str(b)
print('b转换之后的类型',type(b1))
# int()函数将其它类型转换成整数类型
c='20'
print(type(c))
c1=int(c)
print('c1转换之后的类型',type(c1))


# float（）函数：将其他类型转换成小数点类型
d='99.50'
print(type(d))
d1=float(d)
print('d转换之后的类型',type(d1))
